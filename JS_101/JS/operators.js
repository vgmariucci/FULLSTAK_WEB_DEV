// = / * + - % ** ++ --

// =  -> Attribution operator
// /  -> Division operator
// *  -> Multiplication operator
// +  -> Sum operator
// -  -> subtraction operator
// ** -> Exponential operator
// %  -> Rest of a division operator
// ++ -> Increment by 1 operator
// -- -> Decrement by 1 operator

// Examples:

let sum = 2 + 5;
console.log("Sum: " + sum);

let subtraction = 7 - 5;
console.log("Subtraction: " + subtraction);

let division = 18 / 3;
console.log("Division: " + division);

let multiplication = 2 * 5;
console.log("Multiplication: " + multiplication);

let rest_of_division = 48 % 6;
console.log("Rest of division: " + rest_of_division);

let exponential = 2 ** 5;
console.log("Exponential: " + exponential);

let increment_by_one = 0;
console.log("Before increment by one: " + increment_by_one);
increment_by_one++;
console.log("After increment by one: " + increment_by_one);

let decrement_by_one = 2;
console.log("Before decrement by one: " + decrement_by_one);

decrement_by_one--;
console.log("After decrement by one: " + decrement_by_one);

