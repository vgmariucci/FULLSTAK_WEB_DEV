lst = ["one", "two", "three", "four", "five", "six"]

print(lst[0]) # print the index 0 of lst
print(lst[2]) # print the index 2 of lst
print(lst[4]) # print the index 4 of lst

print(lst[0:3]) # print the index 0 to 3 of lst

print(lst[3:5]) # print the index 3 to 5 of lst

print(lst[-1]) # print the index 0 minus 1 of lst which is the last element

print(lst[-2::]) # print the las 2 indexes of lst (this is an example of slicing)

